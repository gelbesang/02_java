package calc.input;

/**
 * 패키지 : calc.input
    실수형 멤버변수 x, y 두 개를 갖는 
    클래스 DoubleInputValue 클래스를 자바빈즈 규격의 클래스로 작성
 * @author Taim
 *
 */
public class DoubleInputValue {

	private double x;
	private double y;
	
	// 기본 생성자
	public DoubleInputValue() {
		
	}
	// get set
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
	
	
	
	
	
	
}
